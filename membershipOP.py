user_input = input("Enter a string: ")

# Check if the letter 'a' is in the string
if 'a' in user_input:
    print("The letter 'a' is in the string.")
else:
    print("The letter 'a' is NOT in the string.")

# Check if the word "Python" is NOT in the string
if "Python" not in user_input:
    print("The word 'Python' is NOT in the string.")
else:
    print("The word 'Python' IS in the string.")